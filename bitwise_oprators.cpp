//bitwise oprators
//AND&& ,| OR,~ NOT,^ XOR
#include<iostream>
using namespace std;
int main()
{
	int a=4;
	int b=6;
	cout<<"a&b "<<(a&b)<<endl;
	cout<<"a|b "<<(a|b)<<endl;
	cout<<"~a "<<(~a)<<endl;
	cout<<"a^b "<<(a^b)<<endl;
	cout<<"right shift oprators "<<(17>>1)<<endl;
	cout<<"right shift oprators "<<(17>>2)<<endl;
	cout<<"left shift oprators "<<(19<<1)<<endl;
	cout<<"left shift oprators "<<(21<<2)<<endl;
	return 0;
}
